package st.netb.mc.mcworld.coordinates;

public abstract class Location {

    protected int x;
    protected int z;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
